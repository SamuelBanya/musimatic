function previous2020Image() {
  if (digitalArt2020ImageIndex <= 0) {
    digitalArt2020ImageIndex = digitalArt2020Array.length - 1;
    digitalArt2020Image.setAttribute("src", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);
    digitalArt2020ImageAnchor.setAttribute("href", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);      
  }
  else {
    digitalArt2020ImageIndex--;
    digitalArt2020Image.setAttribute("src", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);
    digitalArt2020ImageAnchor.setAttribute("href", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);    
  }
}

function random2020Image() {
  var digitalArt2020ImageIndex = Math.floor(Math.random()*digitalArt2020Array.length);
  digitalArt2020Image.setAttribute("src", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);
  digitalArt2020ImageAnchor.setAttribute("href", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);  
}

function next2020Image() {
  if (digitalArt2020ImageIndex >= digitalArt2020Array.length - 1) {
    digitalArt2020ImageIndex = 0;
    digitalArt2020Image.setAttribute("src", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);
    digitalArt2020ImageAnchor.setAttribute("href", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);    
  }
  else {
    digitalArt2020ImageIndex++;
    digitalArt2020Image.setAttribute("src", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);
    digitalArt2020ImageAnchor.setAttribute("href", "images/Sketches2020pngs/" + digitalArt2020Array[digitalArt2020ImageIndex]);    
  }
}
digitalArt2020BackButton.onclick = function() {
  previous2020Image();
}

digitalArt2020RandomButton.onclick = function() {
  random2020Image();
}

digitalArt2020ForwardButton.onclick = function() {
  next2020Image();
}

